# Create a tuple called names that contains several pet names. For instance:

# Name
# Asta
# Butterscotch
# Pudding
# Neptune
# Darwin

names = ('Asta', 'Butterscotch', 'Pudding', 'Neptune', 'Darwin')
print(type(names))          # <class 'tuple'>
print(names)                # ('Asta', 'Butterscotch', 'Pudding', 'Neptune', 'Darwin')



# A more readable solution:

names = (
    'Asta',
    'Butterscotch',
    'Pudding',
    'Neptune',
    'Darwin',
)
